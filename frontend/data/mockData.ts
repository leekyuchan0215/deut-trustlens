function round1(n: number): number {
  return Math.round(n * 10) / 10;
}

function grade(score: number): string {
  if (score >= 90) return "신뢰도 매우 높음";
  if (score >= 75) return "신뢰도 높음";
  if (score >= 60) return "신뢰도 보통";
  if (score >= 40) return "신뢰도 낮음";
  return "신뢰도 매우 낮음";
}

interface BreakdownInput {
  evidence_support_score: number;
  source_quality_score: number;
  consensus_score: number;
  logic_score: number;
  freshness_score: number;
  contradiction_penalty: number;
  risk_penalty: number;
  score_reasons: TrustScoreBreakdown["score_reasons"];
  strengths: string[];
  deductions: string[];
  calculation_detail: TrustScoreBreakdown["calculation_detail"];
}

function buildBreakdown(input: BreakdownInput): TrustScoreBreakdown {
  const base =
    0.4 * input.evidence_support_score +
    0.2 * input.source_quality_score +
    0.15 * input.consensus_score +
    0.15 * input.logic_score +
    0.1 * input.freshness_score;
  const base_score = round1(base);
  const total_score = round1(
    Math.min(100, Math.max(0, base_score - input.contradiction_penalty - input.risk_penalty)),
  );
  return {
    evidence_support_score: input.evidence_support_score,
    source_quality_score: input.source_quality_score,
    consensus_score: input.consensus_score,
    logic_score: input.logic_score,
    freshness_score: input.freshness_score,
    base_score,
    contradiction_penalty: input.contradiction_penalty,
    risk_penalty: input.risk_penalty,
    total_score,
    grade: grade(total_score),
    score_reasons: input.score_reasons,
    strengths: input.strengths,
    deductions: input.deductions,
    calculation_detail: input.calculation_detail,
    formula_version: "1.1",
  };
}

